class Solution
{
    public:
    int NumberOfPath(int a, int b){
        int total = a + b - 2;
        int ans = 1;
        for(int i=1; i<=a-1; i++){
            ans = (ans * total)/i;
            total--;
        }
        return ans;
    }
};


