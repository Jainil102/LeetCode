class Solution
{
    public:
    int NumberOfPath(int a, int b){
        if(a==1 || b==1) return 1;
        return NumberOfPath(a,b-1) + NumberOfPath(a-1,b);
    }
};


//{ Driver Code Starts.
int main()
{
    //taking total testcases
    int t;
    cin>>t;
    while(t--)
    {
        //taking dimensions of the matrix
        int a,b;
        cin>>a>>b;
        Solution ob;
        //calling NumberOfPath() function
        cout << ob.NumberOfPath(a,b) << endl;
    }
}


